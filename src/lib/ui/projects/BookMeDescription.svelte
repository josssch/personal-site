<h2>The Goal</h2>

<p>
    The goal is to bring the calendar right to the forefront of the customer's attention. This way,
    repeat customers—or even new ones—can book an appointment wherever they see fit.
</p>

<p>
    This works well for professions or freelancers where
    <strong>bookings are not confined to a closed circle.</strong>
    Some of these professions might be:
</p>

<ul>
    <li>Massage therapists</li>
    <li>Personal trainers</li>
    <li>School Tutors</li>
    <li>Live Bands</li>
</ul>

<p>
    Freelancers may customize their booking page with a description, some previous work, and hosted
    reviews. This is all displayed right next to the calendar, which is the central focus of the
    page for customers looking to book an appointment.
</p>

<h2>Privacy?</h2>

<p>
    By design, this website only divulges information needed to render the calendar. Back-to-back
    appointments are merged into one on the backend before rendered. The freelancer is also allowed
    to specify things like:
</p>

<ol>
    <li>Prep time, which is time automatically blocked off pre-booking</li>
    <li>Settle time, which is time automatically blocked off post-booking</li>
    <li>Blocked off times, for holidays or times they know they'll be unavailable</li>
    <li>Max booking date, to prevent people scheduling too far in advance</li>
</ol>

<p>The site also disallows people to go back in time to see previous calendars.</p>

<h2>Security?</h2>

<p>There are two main obvious problems here:</p>

<ol>
    <li>Anybody can freely schedule whenever they want</li>
    <li>You don't know who you're hiring</li>
</ol>

<p>
    The first one has a simple solution: <strong>require payment to reserve a spot.</strong>
    Paying in advance allows additional security for the freelancer as well, as it's now possible to
    charge for no-shows, or even last-minute cancelations.
</p>

<p>
    But this creates room for scams, and ties into the second problem. Introducing
    <strong>a verification system and reviews</strong> can assist with this. It's also possible to open
    a chat with the freelancer before scheduling just to make sure they're right for you.
</p>

